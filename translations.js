  /**
   * Get translation for a key, with optional interpolation
   * Usage: SyneriaI18n.t('greeting', {name: 'John'}) => "Hello, John!"
   */
  t(key, paramsOrDefault) {
    const lang = this.translations[this.currentLang] || this.translations.es;
    let val = lang[key] || this.translations.es[key];
    if (!val) {
      // If no translation found, use default string param or key itself
      val = (typeof paramsOrDefault === 'string') ? paramsOrDefault : key;
    }
    if (paramsOrDefault && typeof paramsOrDefault === 'object') {
      Object.keys(paramsOrDefault).forEach(k => {
        val = val.replace(new RegExp('\\{' + k + '\\}', 'g'), paramsOrDefault[k]);
      });
    }
    return val;
  },

  /**
   * Apply translations to all elements with data-i18n attribute
   */
  apply(lang) {
    if (lang) this.currentLang = lang;
    document.documentElement.lang = this.currentLang;

    // Handle RTL for Arabic
    if (this.currentLang === 'ar') {
      document.documentElement.dir = 'rtl';
    } else {
      document.documentElement.dir = 'ltr';
    }

    // Translate all elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const val = this.t(key);
      if (val !== key) {
        // Check if value contains HTML
        if (/<[a-z][\s\S]*>/i.test(val)) {
          el.innerHTML = val;
        } else {
          el.textContent = val;
        }
      }
    });

    // Translate elements with data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = this.t(key);
    });

    // Update page title — check for data-i18n-title first, then fallback
    const titleEl = document.querySelector('[data-i18n-title]');
    if (titleEl) {
      const titleKey = titleEl.getAttribute('data-i18n-title');
      const titleVal = this.t(titleKey);
      if (titleVal !== titleKey) document.title = titleVal;
    } else {
      const titleMap = {
        es: 'Syneria — Tu talento, tu mundo, tu economía',
        en: 'Syneria — Your talent, your world, your economy',
        pt: 'Syneria — Seu talento, seu mundo, sua economia',
        fr: 'Syneria — Votre talent, votre monde, votre économie',
        de: 'Syneria — Dein Talent, deine Welt, deine Wirtschaft',
        ar: 'Syneria — موهبتك، عالمك، اقتصادك',
      };
      if (titleMap[this.currentLang]) {
        document.title = titleMap[this.currentLang];
      }
    }

    // Dispatch event for other scripts to react
    window.dispatchEvent(new CustomEvent('syneria-lang-changed', { detail: { lang: this.currentLang } }));
  },

